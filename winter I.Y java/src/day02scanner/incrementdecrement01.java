package day02scanner;

public class incrementdecrement01 {

	public static void main(String[] args) {
		// If you increase the value of a variable, the process called increment 
		
		
		int num1 = 5;
		//increase the value of num1 by 3
		num1 = num1 + 3;
		// now num1 is 8
		
		// 2. Way
		num1+=3; // this is shorter and better
		// now num1 is 11
		
		//3 way ( just for 1+
		num1++; // now +1 so its 12
		
		System.out.println(num1);
		
		// can be done also multiplication
		
		num1 = num1 * 12; // 144
		System.out.println(num1);
		
		//same logic for decrement...3 ways respectively
		
		// Decrement can be done by division as well. 
		
		num1 /= 10; //
		System.out.println(num1);//14 (14,4 normally but you know...)
		
		
		//Well done
		
		
		

	}

}
