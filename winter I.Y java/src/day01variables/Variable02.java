package day01variables;

public class Variable02 {

	public static void main(String[] args) {
		
		
		char harf1 = 'a';
		char harf2 = 'b';
		System.out.println(harf1 + harf2); // asci degerini topladi
		
		// ab seklinde yazmasi icin :
		
		System.out.print(harf1);
		System.out.println(harf2);
		
		
		// bir char in sayi degeri ascii tablosuna bakmadan nasil bilunur:
		
		int aCharDegeri = 'a';
		System.out.println(aCharDegeri); // 97
		
		int asciiDegerAsterix = '*';
		System.out.println(asciiDegerAsterix); // 42
				
		
		

	}

}
